package com.vw.visitreporting.entity.referencedata;

public class DynamicKpiBrand {
	
	private Long kpiBrandId;
	private Integer brandId;
	private float benchMark;
	
	public Integer getBrandId() {
		return brandId;
	}
	public void setBrandId(Integer brandId) {
		this.brandId = brandId;
	}
	public float getBenchMark() {
		return benchMark;
	}
	public void setBenchMark(float benchMark) {
		this.benchMark = benchMark;
	}
	public Long getKpiBrandId() {
		return kpiBrandId;
	}
	public void setKpiBrandId(Long kpiBrandId) {
		this.kpiBrandId = kpiBrandId;
	}
	
	
}
